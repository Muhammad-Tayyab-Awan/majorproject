let locationEvent = document.querySelector(".addCountryEvent");
let cityList = document.querySelector("select[name='listing[location]']");

locationEvent.addEventListener("change", (event) => {
  console.log(cityList.children);
  let selectedCountry = event.target.value;
  let code = getCode(selectedCountry);
  cityList.innerHTML = "";
  generateOption(code);
});
function getCode(countryName) {
  for (const key in countries) {
    if (countries[key].name === countryName) {
      return key;
    }
  }
}
function generateOption(code) {
  for (const city of countries[code].cities) {
    let option = document.createElement("option");
    option.innerHTML = `<option value="${city} ">
                   ${city}
                </option>`;
    cityList.appendChild(option);
  }
}
